#[derive(PartialEq,Copy,Clone,Debug)]
pub enum DHGroupCode {
    DhGroup15,
    DhGroup16,
    DhGroup17,
    DhGroup18,
    Unknown,
}



impl From<u16> for DHGroupCode {
    #[inline]
    fn from(value: u16) -> DHGroupCode {
        match value {
            0x0101 => DHGroupCode::DhGroup15,
            0x0102 => DHGroupCode::DhGroup16,
            0x0103 => DHGroupCode::DhGroup17,
            0x0104 => DHGroupCode::DhGroup18,
            _ => DHGroupCode::Unknown,
        }
    }
}

#[derive(Copy,Clone)]
pub struct DHGroupElement<'a> {
    pub code: DHGroupCode,
    pub is_safe_prime: bool,
    pub prime_gp_array: &'a [u8],
    pub order_gp_array: &'a [u8],
}   

impl<'a> DHGroupElement<'a>  {
    pub fn from(value: u16) -> Option<&'static Self> {
		DH_GROUPS.iter().find(|x| x.code == DHGroupCode::from(value))
	}
}

impl<'a> Default for DHGroupElement<'a> {
    fn default() -> DHGroupElement<'a> { 
        DH_GROUP15
    }
}

